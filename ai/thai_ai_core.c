int thai_ai_get_system_info(char *info, usize max_size) {
    char buf[2048];
    snprintf(buf, sizeof(buf),
        "ThaiOS v%d.%d.%d\n"
        "Architecture: x86_64\n"
        "Kernel: Microkernel Hybrid\n"
        "Memory: %llu MB free\n"
        "Processes: %d running\n"
        "Uptime: TODO\n",
        THAIOS_VERSION_MAJOR, THAIOS_VERSION_MINOR, THAIOS_VERSION_PATCH,
        pmm_free_pages_count() * 4 / 1024,
        0  // TODO: process count
    );
    snprintf(info, max_size, "%s", buf);
    return SUCCESS;
}

int thai_ai_create_automation(const char *name, const char *trigger, const char *action) {
    if (g_ai.automation_count >= THAI_AI_MAX_AUTOMATIONS) return -ENOMEM;

    ai_automation_t *a = &g_ai.automations[g_ai.automation_count];
    strncpy(a->name, name, sizeof(a->name) - 1);
    strncpy(a->trigger, trigger, sizeof(a->trigger) - 1);
    strncpy(a->action, action, sizeof(a->action) - 1);
    a->enabled = true;
    a->trigger_count = 0;
    g_ai.automation_count++;

    kprintf("[THAI_AI] Automation created: '%s' -> '%s'\n", trigger, action);
    return SUCCESS;
}
